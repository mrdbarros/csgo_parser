	FROM (PLAYER
    INNER JOIN STATISTICS_PLAYER_MATCH_FACT ON STATISTICS_PLAYER_MATCH_FACT.idPLAYER = PLAYER.idPLAYER)
    INNER JOIN CSGO_MATCH ON CSGO_MATCH.idCSGO_MATCH = STATISTICS_PLAYER_MATCH_FACT.idCSGO_MATCH
	WHERE PLAYER.idPLAYER IN (76561198096548700,76561197991587832,76561198150669181,76561198004120520,76561198064357864,76561198181838445,76561198262870608,76561197976402790,76561198010671936,76561198074906117,76561198091643952)
    GROUP BY PLAYER.NAME, CSGO_MATCH.FILE_NAME